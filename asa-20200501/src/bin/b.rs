const BIG_STACK_SIZE: bool = true;
/// Ported from:
/// - [bluss/permutohedron](https://github.com/bluss/permutohedron)
/// - [hatoo/competitive-rust-snippets](https://github.com/hatoo/competitive-rust-snippets)
pub trait LexicalPermutation {
    /// Return  if the slice was permuted, Error: Command failed: false if it is already
    /// at the last ordered permutation.
    fn next_permutation(&mut self) -> bool;
    /// Return  if the slice was permuted, Error: Command failed: false if it is already
    /// at the first ordered permutation.
    fn prev_permutation(&mut self) -> bool;
}
impl<T> LexicalPermutation for [T]
where
    T: Ord,
{
    /// Original author in Rust: Thomas Backman <serenity@exscape.org>
    fn next_permutation(&mut self) -> bool {
        if self.len() < 2 {
            return false;
        }
        let mut i = self.len() - 1;
        while i > 0 && self[i - 1] >= self[i] {
            i -= 1;
        }
        if i == 0 {
            return false;
        }
        let mut j = self.len() - 1;
        while j >= i && self[j] <= self[i - 1] {
            j -= 1;
        }
        self.swap(j, i - 1);
        self[i..].reverse();
        true
    }
    fn prev_permutation(&mut self) -> bool {
        if self.len() < 2 {
            return false;
        }
        let mut i = self.len() - 1;
        while i > 0 && self[i - 1] <= self[i] {
            i -= 1;
        }
        if i == 0 {
            return false;
        }
        self[i..].reverse();
        let mut j = self.len() - 1;
        while j >= i && self[j - 1] < self[i - 1] {
            j -= 1;
        }
        self.swap(i - 1, j);
        true
    }
}
fn main() {
    use std::thread;
    if BIG_STACK_SIZE {
        thread::Builder::new()
            .stack_size(32 * 1024 * 1024)
            .name("solve".into())
            .spawn(solve)
            .unwrap()
            .join()
            .unwrap();
    } else {
        solve();
    }
}
fn solve() {
    let n = get!(usize);
    let xy = get!([(f64, f64); n]);
    let mut perm = (0..n).collect::<Vec<usize>>();
    let mut pat = 1;
    let mut sum_d = 0.0;

    for i in 0..(n - 1) {
        let (fx, fy) = xy[perm[i]];
        let (tx, ty) = xy[perm[i + 1]];
        sum_d += ((tx - fx).powi(2i32) + (ty - fy).powi(2i32)).sqrt();
    }

    while perm.next_permutation() {
        pat += 1;
        for i in 0..(n - 1) {
            let (fx, fy) = xy[perm[i]];
            let (tx, ty) = xy[perm[i + 1]];
            sum_d += ((tx - fx).powi(2i32) + (ty - fy).powi(2i32)).sqrt();
        }
    }

    echo!(sum_d / pat as f64);
}
